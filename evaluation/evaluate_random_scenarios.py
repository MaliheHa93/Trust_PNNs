# ---------------------------------------------------------------------
# Utility functions
# ---------------------------------------------------------------------

def save_csv(
    path: Path,
    rows: list[dict],
) -> None:
    """
    Save experiment results to a CSV file.
    """

    if not rows:
        raise ValueError(
            "No rows to save."
        )

    with path.open(
        "w",
        newline="",
    ) as file:

        writer = csv.DictWriter(
            file,
            fieldnames=list(
                rows[0].keys()
            ),
        )

        writer.writeheader()

        writer.writerows(
            rows
        )


def decide_from_score(
    score: float,
    theta_accept: float,
    theta_reject: float,
) -> str:
    """
    Apply the three-way decision policy.

    High score:
        Accept locally.

    Low score:
        Reject.

    Intermediate score:
        Forward to fog.
    """

    if score >= theta_accept:
        return "accept"

    if score < theta_reject:
        return "reject"

    return "forward_to_fog"


# ---------------------------------------------------------------------
# Invocation construction
# ---------------------------------------------------------------------

def make_modified_invocation(
    original_invocation,
    value: float,
    confidence: float,
    delay_s: float,
):
    """
    Create a copy-like invocation object containing the fields
    required by TrustManager.

    The original phys-MCP implementation is not modified.

    The output value, confidence, and execution delay are replaced
    with the values generated by the experimental scenario.
    """

    return SimpleNamespace(

        backend_id=getattr(
            original_invocation,
            "backend_id",
            "chemical-backend",
        ),

        task_id=getattr(
            original_invocation,
            "task_id",
            "random-scenario-task",
        ),

        output_payload={
            "value":
                value,

            "modality":
                "chemical_concentration",
        },

        confidence=
            confidence,

        execution_latency_ms=
            delay_s * 1000.0,

        backend_state=getattr(
            original_invocation,
            "backend_state",
            "ready",
        ),

        notes=getattr(
            original_invocation,
            "notes",
            None,
        ),
    )


# ---------------------------------------------------------------------
# Randomized evaluation
# ---------------------------------------------------------------------

def evaluate(
    seed: int | None = None,
) -> list[dict]:
    """
    Evaluate the trust layer over a large set of randomized PNN
    output scenarios.

    Ground-truth physical noise and drift are used only to generate
    the PNN output.

    TrustManager receives imperfect estimates of noise and drift
    generated by scenario_generator.py.

    This avoids giving the trust layer direct access to the exact
    uncertainty parameters used to corrupt the physical output.

    Parameters
    ----------
    seed:
        Optional externally supplied random seed.

        When seed is None, the default seed from ExperimentConfig
        is used. This preserves the normal single-seed experiment.

        Multi-seed evaluation can call, for example:

            evaluate(seed=42)
            evaluate(seed=101)
            evaluate(seed=202)
    """

    # -----------------------------------------------------------------
    # Shared experiment configuration
    # -----------------------------------------------------------------

    cfg = (
        ExperimentConfig()
    )

    # -----------------------------------------------------------------
    # Resolve random seed
    #
    # Normal evaluation:
    #
    #     evaluate()
    #
    # uses cfg.seed.
    #
    # Multi-seed evaluation:
    #
    #     evaluate(seed=101)
    #
    # overrides cfg.seed without modifying ExperimentConfig.
    # -----------------------------------------------------------------

    effective_seed = (
        cfg.seed
        if seed is None
        else seed
    )

    # scenario_generator.py uses Python's global random generator.
    # Setting the seed here ensures reproducibility.
    random.seed(
        effective_seed
    )

    # -----------------------------------------------------------------
    # Create phys-MCP orchestration path
    # -----------------------------------------------------------------

    orchestrator = (
        build_default_orchestrator()
    )

    task = make_chemical_task(
        task_id=(
            "random-scenario-chemical"
        ),
        input_level=1.8,
    )

    base_run = (
        orchestrator.execute_task(
            task
        )
    )

    if (
        not base_run.success
        or base_run.invocation
        is None
    ):

        raise RuntimeError(
            "phys-MCP invocation failed: "
            f"{base_run.failure_reason}"
        )

    # -----------------------------------------------------------------
    # Trust configuration
    # -----------------------------------------------------------------

    theta_accept = (
        cfg.theta_accept
    )

    theta_reject = (
        cfg.theta_reject
    )

    trust_manager = (
        TrustManager(

            TrustConfig(

                alpha=
                    cfg.alpha,

                beta=
                    cfg.beta,

                gamma=
                    cfg.gamma,

                delta=
                    cfg.delta,

                tau_s=
                    cfg.tau_s,

                theta_accept=
                    theta_accept,

                theta_reject=
                    theta_reject,
            )
        )
    )

    rows: list[
        dict
    ] = []

    # -----------------------------------------------------------------
    # Generate and evaluate randomized scenarios
    # -----------------------------------------------------------------

    for scenario_id in range(
        cfg.random_scenarios
    ):

        # -------------------------------------------------------------
        # Generate one randomized PNN scenario.
        #
        # scenario.true_noise
        # scenario.true_drift
        #
        #     Hidden physical conditions used to generate the output.
        #
        # scenario.noise
        # scenario.drift
        #
        #     Imperfect estimated telemetry visible to TrustManager.
        # -------------------------------------------------------------

        scenario = (
            make_random_scenario(

                scenario_id=
                    scenario_id,

                true_value=
                    cfg.true_value,

                error_threshold=
                    cfg.error_threshold,

                correlated_confidence=
                    True,
            )
        )

        # -------------------------------------------------------------
        # Construct the PNN invocation
        # -------------------------------------------------------------

        invocation = (
            make_modified_invocation(

                original_invocation=
                    base_run.invocation,

                value=
                    scenario.predicted_value,

                confidence=
                    scenario.confidence,

                delay_s=
                    scenario.delay_s,
            )
        )

        # -------------------------------------------------------------
        # Construct telemetry visible to the trust layer
        #
        # IMPORTANT:
        #
        # These are ESTIMATED noise/drift values.
        #
        # We do NOT pass:
        #
        #     scenario.true_noise
        #     scenario.true_drift
        #
        # to TrustManager.
        # -------------------------------------------------------------

        telemetry = dict(
            base_run.telemetry_after
        )

        telemetry.update(
            {
                "noise_score":
                    scenario.noise,

                "drift_score":
                    scenario.drift,

                "age_of_information_ms":
                    scenario.delay_s
                    * 1000.0,

                "output_modality":
                    "chemical_concentration",
            }
        )

        # -------------------------------------------------------------
        # Apply proposed trust layer
        # -------------------------------------------------------------

        trust_result = (
            trust_manager
            .evaluate_invocation(

                invocation=
                    invocation,

                telemetry_after=
                    telemetry,
            )
        )

        freshness = (
            trust_result
            .evidence
            .freshness
        )

        full_trust_score = (
            trust_result
            .evidence
            .trust
        )

        # -------------------------------------------------------------
        # Baseline methods
        # -------------------------------------------------------------

        methods = {

            # ---------------------------------------------------------
            # No post-invocation trust management.
            # ---------------------------------------------------------

            "raw_phys_mcp_output": {

                "decision":
                    "accept",

                "method_score":
                    None,
            },

            # ---------------------------------------------------------
            # Confidence-only decision.
            # ---------------------------------------------------------

            "confidence_only": {

                "decision":
                    decide_from_score(

                        scenario.confidence,

                        theta_accept,

                        theta_reject,
                    ),

                "method_score":
                    scenario.confidence,
            },

            # ---------------------------------------------------------
            # Freshness-only decision.
            # ---------------------------------------------------------

            "freshness_only": {

                "decision":
                    decide_from_score(

                        freshness,

                        theta_accept,

                        theta_reject,
                    ),

                "method_score":
                    freshness,
            },

            # ---------------------------------------------------------
            # Proposed multi-factor trust score.
            # ---------------------------------------------------------

            "full_trust_score": {

                "decision":
                    trust_result
                    .decision
                    .value,

                "method_score":
                    full_trust_score,
            },
        }

        # -------------------------------------------------------------
        # Store results for every method
        # -------------------------------------------------------------

        for (
            method_name,
            method_info,
        ) in methods.items():

            decision = (
                method_info[
                    "decision"
                ]
            )

            method_score = (
                method_info[
                    "method_score"
                ]
            )

            accepted = (
                decision
                == "accept"
            )

            rejected = (
                decision
                == "reject"
            )

            forwarded = (
                decision
                == "forward_to_fog"
            )

            false_acceptance = (
                accepted
                and scenario.bad_output
            )

            false_rejection = (
                rejected
                and not scenario.bad_output
            )

            rows.append(
                {
                    # ---------------------------------------------
                    # Random seed
                    # ---------------------------------------------

                    "seed":
                        effective_seed,

                    # ---------------------------------------------
                    # Scenario and method
                    # ---------------------------------------------

                    "scenario_id":
                        scenario.scenario_id,

                    "method":
                        method_name,

                    # ---------------------------------------------
                    # Hidden ground-truth physical conditions
                    #
                    # Used only for analysis/evaluation.
                    # ---------------------------------------------

                    "true_noise":
                        scenario.true_noise,

                    "true_drift":
                        scenario.true_drift,

                    # ---------------------------------------------
                    # Imperfect telemetry estimates
                    #
                    # These are what TrustManager actually sees.
                    # ---------------------------------------------

                    "estimated_noise":
                        scenario.noise,

                    "estimated_drift":
                        scenario.drift,

                    # Keep old names as aliases if existing plotting
                    # scripts expect noise_level/drift_level.

                    "noise_level":
                        scenario.true_noise,

                    "drift_level":
                        scenario.true_drift,

                    # ---------------------------------------------
                    # Other evidence parameters
                    # ---------------------------------------------

                    "confidence":
                        scenario.confidence,

                    "delay_s":
                        scenario.delay_s,

                    "freshness":
                        freshness,

                    # ---------------------------------------------
                    # Trust information
                    # ---------------------------------------------

                    "full_trust_score":
                        full_trust_score,

                    "method_score":
                        method_score,

                    # ---------------------------------------------
                    # Ground-truth output information
                    # ---------------------------------------------

                    "true_value":
                        scenario.true_value,

                    "predicted_value":
                        scenario.predicted_value,

                    "absolute_error":
                        scenario.absolute_error,

                    "bad_output":
                        scenario.bad_output,

                    # ---------------------------------------------
                    # Decision information
                    # ---------------------------------------------

                    "decision":
                        decision,

                    "accepted":
                        accepted,

                    "rejected":
                        rejected,

                    "forwarded":
                        forwarded,

                    "false_acceptance":
                        false_acceptance,

                    "false_rejection":
                        false_rejection,
                }
            )

    return (
        rows
    )


# ---------------------------------------------------------------------
# Main entry point
# ---------------------------------------------------------------------

def main() -> None:
    """
    Run the standard single-seed randomized evaluation.

    The default seed is obtained from ExperimentConfig.

    Multi-seed evaluation calls evaluate(seed=...) directly and
    therefore does not execute this function.
    """

    rows = (
        evaluate()
    )

    output_path = (
        RESULTS_DIR
        / "random_scenario_results.csv"
    )

    save_csv(
        output_path,
        rows,
    )

    used_seed = (
        rows[0][
            "seed"
        ]
        if rows
        else None
    )

    print()

    print(
        "Random scenario evaluation completed."
    )

    print(
        f"Random seed: "
        f"{used_seed}"
    )

    print(
        f"Saved random scenario results to: "
        f"{output_path}"
    )

    print(
        f"Total randomized scenarios: "
        f"{len(rows) // 4}"
    )


if __name__ == "__main__":
    main()